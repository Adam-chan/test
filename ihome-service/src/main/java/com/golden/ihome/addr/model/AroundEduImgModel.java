package com.golden.ihome.addr.model;

import java.io.Serializable;
import java.util.Date;

/**
 * @author lb
 * 教育设施图片表
 * 时间：2016-10-21
 */
public class AroundEduImgModel implements Serializable{
	
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String folderName;
	private String fileName;
	private Date createTime;
	private Integer eduId;
	private String allFileAddr;
	
	public String getAllFileAddr() {
		return allFileAddr;
	}
	public void setAllFileAddr(String allFileAddr) {
		this.allFileAddr = allFileAddr;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getFolderName() {
		return folderName;
	}
	public void setFolderName(String folderName) {
		this.folderName = folderName;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Integer getEduId() {
		return eduId;
	}
	public void setEduId(Integer eduId) {
		this.eduId = eduId;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
}