package com.golden.ihome.addr.model;

import java.io.Serializable;
import java.util.List;

/**
 * 
 * @author lza
 * 类	名：楼盘树形菜单类
 * 类   描	述：显示楼盘下的下拉菜单
 * 日   	期：2016年12月25日
 */
public class PremisesMenuModel implements Serializable{
	
	private static final long serialVersionUID = 1220955459921754089L;
	private String groupName;//分组名称
	private String buildingName;//楼栋名称
	private Integer buildingId;//楼栋id
	private Integer groupId;//分组id
	private Integer statistics;//单元个数
	private List<PremisesMenuModel> listMenu;//子导航
	private Integer unitId;//单元id
	private String unitCode;//单元编号
	private List<PremisesMenuModel> unitMenu;//单元子导航
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getBuildingName() {
		return buildingName;
	}
	public void setBuildingName(String buildingName) {
		this.buildingName = buildingName;
	}
	public Integer getBuildingId() {
		return buildingId;
	}
	public void setBuildingId(Integer buildingId) {
		this.buildingId = buildingId;
	}
	public Integer getGroupId() {
		return groupId;
	}
	public void setGroupId(Integer groupId) {
		this.groupId = groupId;
	}
	public Integer getStatistics() {
		return statistics;
	}
	public void setStatistics(Integer statistics) {
		this.statistics = statistics;
	}
	public List<PremisesMenuModel> getListMenu() {
		return listMenu;
	}
	public void setListMenu(List<PremisesMenuModel> listMenu) {
		this.listMenu = listMenu;
	}
	public Integer getUnitId() {
		return unitId;
	}
	public void setUnitId(Integer unitId) {
		this.unitId = unitId;
	}
	public String getUnitCode() {
		return unitCode;
	}
	public void setUnitCode(String unitCode) {
		this.unitCode = unitCode;
	}
	public List<PremisesMenuModel> getUnitMenu() {
		return unitMenu;
	}
	public void setUnitMenu(List<PremisesMenuModel> unitMenu) {
		this.unitMenu = unitMenu;
	}
	
}
