package com.golden.ihome.addr.model;

import java.io.Serializable;
import java.sql.Date;

/**
* @author weizhichao
 * 类名称：楼盘管理--楼盘详情--楼盘图片Model
 * 类描述：楼盘图片
 * 时间：2016-10-24
 */
public class PremisesPicturesModel implements Serializable{

	private static final long serialVersionUID = -390491536102415718L;
	
	/**
	 * id
	 */
	private Integer id;
	/**
	 * 楼盘id
	 */
	private Integer premisesId;
	/**
	 * 文件夹名称
	 */
	private String folderName;
	/**
	 * 文件名称
	 */
	private String fileName;
	/**
	 * 服务器上的文件名称
	 */
	private String fileNameLong;
	/**
	 * 上传时间
	 */
	private Date createTime;
	/**
	 * 图片状态    1使用，2删除
	 */
	private Integer status;
	/**
	 * 是否封面    1不是，2是封面
	 */
	private Integer isCover;
	/**
	 * 图片类型    1总规,2位置,3外观,4景观,5栋座外观,6平曾剖面图,7原始户型图
	 */
	private Integer picType;
	/**
	 * 根据picType查询的分组数
	 */
	private Integer count;
	/**
	 * 代表图1是代表图，2不是代表图
	 */
	private Integer representPic;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getPremisesId() {
		return premisesId;
	}
	public void setPremisesId(Integer premisesId) {
		this.premisesId = premisesId;
	}
	public String getFolderName() {
		return folderName;
	}
	public void setFolderName(String folderName) {
		this.folderName = folderName;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileNameLong() {
		return fileNameLong;
	}
	public void setFileNameLong(String fileNameLong) {
		this.fileNameLong = fileNameLong;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Integer getIsCover() {
		return isCover;
	}
	public void setIsCover(Integer isCover) {
		this.isCover = isCover;
	}
	public Integer getPicType() {
		return picType;
	}
	public void setPicType(Integer picType) {
		this.picType = picType;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public Integer getRepresentPic() {
		return representPic;
	}
	public void setRepresentPic(Integer representPic) {
		this.representPic = representPic;
	}
	
	
}
