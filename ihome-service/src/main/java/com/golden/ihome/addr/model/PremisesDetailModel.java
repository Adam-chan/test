package com.golden.ihome.addr.model;

import java.io.Serializable;
/**
 * 销售指南
 * @author weizhichao
 *
 */
public class PremisesDetailModel implements Serializable{

	private static final long serialVersionUID = -1267689526133302049L;
	/**
	 * id序列
	 */
	private Integer id;
	/**
	 * 楼盘id
	 */
	private Integer premisesId;
	/**
	 * 客户认定标准注意事项
	 */
	private String customerStandard;
	/**
	 * 带看对接方式及注意事项
	 */
	private String linkWays;
	/**
	 * 推广方式建议
	 */
	private String promotionAdvice;
	/**
	 * 其他特殊需求或安排
	 */
	
	private String otherWays;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getPremisesId() {
		return premisesId;
	}
	public void setPremisesId(Integer premisesId) {
		this.premisesId = premisesId;
	}
	public String getCustomerStandard() {
		return customerStandard;
	}
	public void setCustomerStandard(String customerStandard) {
		this.customerStandard = customerStandard;
	}
	public String getLinkWays() {
		return linkWays;
	}
	public void setLinkWays(String linkWays) {
		this.linkWays = linkWays;
	}
	public String getPromotionAdvice() {
		return promotionAdvice;
	}
	public void setPromotionAdvice(String promotionAdvice) {
		this.promotionAdvice = promotionAdvice;
	}
	public String getOtherWays() {
		return otherWays;
	}
	public void setOtherWays(String otherWays) {
		this.otherWays = otherWays;
	}
	

}
